mp_type eta_crunch(mp_type z, mp_type eta)
{
	mp_type t1 , t2 , t8 , t20 ;

	t1 = z * z;
    t2 = t1 * z;
    t8 = sin(eta);
    t20 = 0.1e1 / (0.1e0 * t2 + 0.5000e4 * t1 + 0.125e2) * (0.1e0 * t2 * eta + 0.5000e4 * t1 * eta + 0.125e2 * eta - 0.1e0 * t2 * t8 - 0.5000e4 * t1 * t8 - 0.125e2 * t8 + 0.6283185312e0 * t2 + 0.785398164e2);


	return t20 ;
}


mp_type eta_bang(mp_type z, mp_type eta)
{
	mp_type t1 , t2 , t8 , t20 ;


	t1 = z * z;
	t2 = t1 * z;
	t8 = sin(eta);
	t20 = 0.1e1 / (0.1e0 * t2 + 0.5000e4 * t1 + 0.125e2) * (0.1e0 * t2 * eta + 0.5000e4 * t1 * eta + 0.125e2 * eta - 0.1e0 * t2 * t8 - 0.5000e4 * t1 * t8 - 0.125e2 * t8 - 0.3141592656e5 * t1);

	return t20 ;
}


mp_type eta_eq(mp_type z, mp_type eta)
{
	mp_type t1 , t6 , t7 , t9 , t11 , t12 , t14 , t16 , t20 ;


	t1 = z * z;
	t6 = pow(0.1e0 * t1 * z + 0.5000e4 * t1 + 0.125e2, 0.1e1 / 0.3e1);
	t7 = t6 * t6;
	t9 = sin(eta);
	t11 = pow(z, 0.1e1 / 0.3e1);
	t12 = t11 * t11;
	t14 = 0.1e1 / t7;
	t16 = sqrt(t14 * t12);
	t20 = t14 * (t7 * eta - t7 * t9 - 0.3141592656e5 * t16 * t12 * z);

	return t20 ;
}

mp_type energy_e1(mp_type z) {
    return -pow(0.8e1, 0.2e1 / 0.3e1) * pow(0.3141592654e1 * z, 0.2e1 / 0.3e1) * pow(0.4e1, 0.1e1 / 0.3e1) * pow(0.1e0 * pow(z, 0.3e1) + 0.5000e4 * z * z + 0.125e2, -0.2e1 / 0.3e1) / 0.8e1;
}

mp_type r_exact(mp_type z, mp_type eta) {
    mp_type a,b;
    a = -z/(2*(energy_e1(z)));
    b=1-cos(eta);
    return a*b;
}



